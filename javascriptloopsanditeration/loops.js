// 1. Our First For Loop Practice
// Print "Da ba dee da ba daa" exactly 6 times
for (let i = 0; i < 6; i++) {
    console.log("Da ba dee da ba daa");
}

// 2. For Loops Practice
// Loop that counts down from 25 to 0 in steps of 5
for (let i = 25; i >= 0; i -= 5) {
    console.log(i);
}

// 3. Iterating Arrays Exercise
// Array of names
const people = ["Scooby", "Velma", "Daphne", "Shaggy", "Fred"];

// Loop over the array and print each name in uppercase
for (let i = 0; i < people.length; i++) {
    console.log(people[i].toUpperCase());
}
